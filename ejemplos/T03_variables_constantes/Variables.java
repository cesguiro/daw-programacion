class Variables {

    public static void main(String[] args) {
        //Tipo variable + nombre
        int a;
        String b;
        boolean c;

        //Asignar valor incial a una variable
        int d = 5;

        //Tipos de datos primitivos
        int e = 3; //Entero con signo. Tamaño (bits): 32
        byte f = -123; //Entero con signo. Tamaño (bits): 8
        short g = 34; //Entero con signo. Tamaño (bits): 16
        long h = -7898; //Entero con signo. Tamaño (bits): 64
        float i = 34.9f; //Real coma flotante con percisión simple (32 bits);
        double j = 345.987; //Real coma flotante con precisión doble (64 bits);
        char k = 'l'; //Carácter
        boolean l = true; //Boolean. Sólo puede tomar el valor verdadero o falso

        //Variables de tipo cadena de caracteres
        String m = "Hola mundo";
    }
}